					<!--<h4 style="margin-top:-20px; margin-bottom:7px;">Summary</h4>-->
                    <strong>R</strong>esearch Articles written in English are invited from interested scholars and researchers in the academic community and other establishments for publication. Authors who wish to submit manuscript should note that the article has not been submitted elsewhere nor is it for consideration in another journal. The article should be the original work of the author.<br/>
The manuscript should be in the following format and sections:
				</div>
				<div class="well wow fadeInDown animated" data-wow-delay=".6s">
				<h4 style="margin-top:-20px; margin-bottom:7px;">Title page</h4>
					The title page should be boldly typed in Times New Romans, 16pts and align centered.<br>Name of corresponding author(s) (to whom proofs and all correspondence will be sent) together with their Department, <br>Name of Organization, City, Postcode, Country, and e-mail address(es).
			</div>
				<div class="well wow fadeInDown animated" data-wow-delay=".7s">
				<h4 style="margin-top:-20px; margin-bottom:7px;">Abstract</h4>
                The abstract should not be more than 250 words. The essential elements of the abstract are:<ul>
              <br><li>Background: A simple opening sentence or two placing the work in context.</li>

<li>Aims: One or two sentences giving the purpose of the work.</li>

<li>Method(s): One or two sentences explaining what was done.</li>

<li>Results: One or two sentences indicating the main findings.</li>

<li>Conclusions: One sentence giving the most important consequence of the work.</li>
                </ul> 
                <br> Keywords 
                <br>
A list of 3–5 words suitable for indexing should be given below the abstract, each separated by a semicolon (;). 
                </div>
                
                <div id="One" class="well wow fadeInDown animated" data-wow-delay=".7s">
              <h4 style="margin-top:-20px; margin-bottom:7px;">Introduction</h4>
					The Introduction should be concise (double spaced, typed). <br>Methods:  <br>The methods section of a research paper provides the information by which a study’s validity is judged. Therefore, it requires a clear and precise description of how an experiment was done, and the rationale for why specific experimental procedures were chosen. <br> The methods section structure should: describe the materials used in the study, explain how the materials were prepared for the study, describe the research protocol, explain how measurements were made and what calculations were performed, and state which statistical tests were done to analyze the data. <br> This section should contain sufficient detail so that all experimental procedures can be repeated by others in conjunction with cited references. Procedural detail that has been published previously should be referred to by citation. When a modified procedure is used, only the author’s modifications of the previously published method need to be given in detail. Subheadings should be used. 
			</div>
            
            <div class="well wow fadeInDown animated" data-wow-delay=".7s">
              <h4 style="margin-top:-20px; margin-bottom:7px;">Results</h4>
					The page length of this section is set by the amount and types of data to be reported. Continue to be concise, using figures and tables, if appropriate, to present results most effectively. <br>Disscussion: <br>Interpret your data in the discussion in appropriate depth. This means that when you explain a phenomenon you must describe mechanisms that may account for the observation. If your results differ from your expectations, explain why that may have happened. If your results agree, then describe the theory that the evidence supported. It is never appropriate to simply state that the data agreed with expectations, and let it drop at that.<br><ul>
                   <br> <li>Decide if each hypothesis is supported, rejected, or if you cannot make a decision with confidence. Do not simply dismiss a study or part of a study as "inconclusive."</li>
                    <li>Research papers are not accepted if the work is incomplete. Draw what conclusions you can based upon the results that you have, and treat the study as a finished work.</li>
                    <li>ou may suggest future directions, such as how the experiment might be modified to accomplish another objective.</li>
                    <li>Explain all of your observations as much as possible, focusing on mechanisms.</li>
                    <li>Decide if the experimental design adequately addressed the hypothesis, and whether or not it was properly controlled.
</li>
                    <li>Try to offer alternative explanations if reasonable alternatives exist.</li>
                    <li>One experiment will not answer an overall question, so keeping the big picture in mind, where do you go next? The best studies open up new avenues of research. What questions remain?</li>
                    <li>Recommendations for specific papers will provide additional suggestions. </li>
 </ul>
			</div>
            <div class="well wow fadeInDown animated" data-wow-delay=".7s">
              <h4 style="margin-top:-20px; margin-bottom:7px;">Conclusions</h4>
					This should summarize the major conclusions that can be drawn, pointing out their significance, and alluding to possible future directions. <br><br>Acknowledgments: <br>These should be brief and should be made only to those who have made a significant contribution to the study. People who contributed to the work but do not fit the criteria for authors should be listed in the Acknowledgments, along with their contributions. You must also ensure that anyone named in the Acknowledgments agrees to being so named.  <br>Name of Organization, City, Postcode, Country, and e-mail address(es).<br><br>References:<br>The APA style of referencing should be used<br><br>One Author<br>Williams, J. H. (2008). Employee engagement: Improving participation in safety. Professional Safety, 53(12), 40-45.<br><br> Two or more authors:<br>Keller, T. E., Cusick, G. R., & Courtney, M. E. (2007). Approaching the transition to adulthood: Distinctive profiles of adolescents aging out of the child welfare system. Social Services Review, 81, 453- 484. <br><br>Magazine article:<br>Mathews, J., Berrett, D., & Brillman, D. (2005, May 16). Other winning equations. Newsweek, 145(20), 58-59. <br><br> Newspaper Article with No Author and Discontinuous Pages :<br>Generic Prozac debuts. (2001, August 3). The Washington Post, pp. E1, E4 <br><br>Figures:<br>Figures should be presented in the order they are mentioned in the text. Figures should be labelled sequentially as Figure 1, Figure 2, etc. The figures should be of high quality, appropriately serve their intended purpose, and have figure legends that adequately explain their meaning. All figures should be clear enough and must be sufficient high-resolution to be photographed by the printer. <br><br>Tables :<br>Begin each table on a separate page, double-spaced. All tables must be numbered consecutively (using Arabic numerals) and be cited in the text as Table 1, Table2, etc., and followed by a short, descriptive title. Abbreviations used in the table must be defined in a footnote to the table. Do not use vertical lines and keep horizontal rules to a minimum. All tables should be clear enough and the units are usually in brackets following the quantity and the font should be Times New Roman and size of 8 pt. 
